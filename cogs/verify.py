import disnake
from disnake.ext import commands
import json

# ‡πÇ‡∏´‡∏•‡∏î‡∏Ñ‡∏≠‡∏ô‡∏ü‡∏¥‡∏Å‡∏à‡∏≤‡∏Å config.json
with open("config/config.json", "r", encoding="utf-8") as f:
    config = json.load(f)

# üîπ ‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡∏Ñ‡πà‡∏≤‡∏ï‡∏±‡∏ß‡πÅ‡∏õ‡∏£
GUILD_IDS = config.get("guild_ids", [])
CHANNEL_ID = config["channel"].get("verify_channel_id", None)
ROLE_IDS = [
    config["roles"].get("verified", None),
    config["roles"].get("member", None)
]
RULES_CHANNEL_ID = config["channel"].get("rules_channel_id", None)

class VerifyButton(disnake.ui.View):
    def __init__(self, roles: list, timeout=None):  # ‡∏õ‡∏¥‡∏î timeout
        super().__init__(timeout=timeout)  # ‡πÑ‡∏°‡πà‡πÉ‡∏´‡πâ‡∏´‡∏°‡∏î‡∏≠‡∏≤‡∏¢‡∏∏
        self.roles = roles

    @disnake.ui.button(label="‚úÖ Verify Me!", style=disnake.ButtonStyle.success, custom_id="verify_button")
    async def verify_callback(self, button: disnake.ui.Button, interaction: disnake.MessageInteraction):
        member = interaction.user
        for role in self.roles:
            if role in member.roles:
                return await interaction.response.send_message(f"‚ö†Ô∏è ‡∏Ñ‡∏∏‡∏ì‡πÑ‡∏î‡πâ‡∏£‡∏±‡∏ö Role {role.mention} ‡πÑ‡∏õ‡πÅ‡∏•‡πâ‡∏ß!", ephemeral=True)

        await member.add_roles(*self.roles)
        await interaction.response.send_message("‚úÖ ‡∏Ñ‡∏∏‡∏ì‡πÑ‡∏î‡πâ‡∏£‡∏±‡∏ö Role Verified ‡πÅ‡∏•‡πâ‡∏ß! ‡∏¢‡∏¥‡∏ô‡∏î‡∏µ‡∏ï‡πâ‡∏≠‡∏ô‡∏£‡∏±‡∏ö!", ephemeral=True)

class Verify(commands.Cog):
    def __init__(self, bot):
        self.bot = bot

    @commands.Cog.listener()
    async def on_ready(self):
        print(f"üìÅ Loaded ({self.__class__.__name__}) Succeed.")

        for guild_id in GUILD_IDS:
            guild = self.bot.get_guild(guild_id)  # ‡∏î‡∏∂‡∏á guild ‡∏ï‡∏≤‡∏° ID
            if not guild:
                print(f"‚ùå ‡πÑ‡∏°‡πà‡∏û‡∏ö‡πÄ‡∏ã‡∏¥‡∏£‡πå‡∏ü‡πÄ‡∏ß‡∏≠‡∏£‡πå ID: {guild_id}")
                return

            channel = guild.get_channel(CHANNEL_ID)
            rules_channel = guild.get_channel(RULES_CHANNEL_ID)

            # ‡∏Å‡∏£‡∏≠‡∏á role ‡∏ó‡∏µ‡πà‡πÄ‡∏õ‡πá‡∏ô None ‡∏≠‡∏≠‡∏Å
            roles = [guild.get_role(role_id) for role_id in ROLE_IDS if role_id]
            if not channel or not roles or not rules_channel:
                print(f"‚ùå ‡πÑ‡∏°‡πà‡∏û‡∏ö Channel ‡∏´‡∏£‡∏∑‡∏≠ Role ‡∏ó‡∏µ‡πà‡∏à‡∏≥‡πÄ‡∏õ‡πá‡∏ô‡πÉ‡∏ô‡πÄ‡∏ã‡∏¥‡∏£‡πå‡∏ü‡πÄ‡∏ß‡∏≠‡∏£‡πå: {guild.name}")
                return

            embed = disnake.Embed(
                title="üîπ ‡∏¢‡∏¥‡∏ô‡∏î‡∏µ‡∏ï‡πâ‡∏≠‡∏ô‡∏£‡∏±‡∏ö‡∏™‡∏π‡πà‡πÄ‡∏ã‡∏¥‡∏£‡πå‡∏ü‡πÄ‡∏ß‡∏≠‡∏£‡πå!",
                description="‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏Ç‡πâ‡∏≤‡∏ñ‡∏∂‡∏á‡πÄ‡∏ô‡∏∑‡πâ‡∏≠‡∏´‡∏≤‡πÅ‡∏•‡∏∞‡∏´‡πâ‡∏≠‡∏á‡πÅ‡∏ä‡∏ó‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏î‡∏¢‡∏∑‡∏ô‡∏¢‡∏±‡∏ô‡∏ï‡∏±‡∏ß‡∏ï‡∏ô‡∏î‡πâ‡∏≤‡∏ô‡∏•‡πà‡∏≤‡∏á",
                color=disnake.Color.blue()
            )
            embed.set_thumbnail(url=guild.icon.url if guild.icon else None)
            embed.add_field(name="üìú ‡∏Å‡∏é‡∏Ç‡∏≠‡∏á‡πÄ‡∏ã‡∏¥‡∏£‡πå‡∏ü‡πÄ‡∏ß‡∏≠‡∏£‡πå", value=f"‡πÇ‡∏õ‡∏£‡∏î‡∏õ‡∏è‡∏¥‡∏ö‡∏±‡∏ï‡∏¥‡∏ï‡∏≤‡∏°‡∏Å‡∏é‡∏Ç‡∏≠‡∏á‡πÄ‡∏£‡∏≤ {rules_channel.mention}", inline=False)
            embed.add_field(name="üéâ ‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡∏û‡∏¥‡πÄ‡∏®‡∏©", value="‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏¢‡∏∑‡∏ô‡∏¢‡∏±‡∏ô‡∏ï‡∏±‡∏ß‡∏ï‡∏ô‡πÅ‡∏•‡πâ‡∏ß ‡∏Ñ‡∏∏‡∏ì‡∏à‡∏∞‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡πÄ‡∏Ç‡πâ‡∏≤‡∏ñ‡∏∂‡∏á‡πÄ‡∏ô‡∏∑‡πâ‡∏≠‡∏´‡∏≤‡πÅ‡∏•‡∏∞‡∏Å‡∏¥‡∏à‡∏Å‡∏£‡∏£‡∏°‡∏ï‡πà‡∏≤‡∏á‡πÜ ‡πÑ‡∏î‡πâ‡∏≠‡∏¢‡πà‡∏≤‡∏á‡πÄ‡∏ï‡πá‡∏°‡∏ó‡∏µ‡πà!", inline=False)
            embed.set_footer(text="‡∏´‡∏≤‡∏Å‡∏°‡∏µ‡∏õ‡∏±‡∏ç‡∏´‡∏≤ ‡πÇ‡∏õ‡∏£‡∏î‡∏ï‡∏¥‡∏î‡∏ï‡πà‡∏≠‡πÅ‡∏≠‡∏î‡∏°‡∏¥‡∏ô üë®‚Äçüíª")

            # ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ó‡∏µ‡πà‡∏™‡πà‡∏á‡πÑ‡∏õ‡πÅ‡∏•‡πâ‡∏ß‡πÉ‡∏ô‡∏ä‡πà‡∏≠‡∏á‡∏ô‡∏±‡πâ‡∏ô‡πÑ‡∏´‡∏°
            async for message in channel.history(limit=10):
                if message.author == self.bot.user and message.embeds:
                    await message.edit(view=VerifyButton(roles))
                    print(f"‚úÖ ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏õ‡∏∏‡πà‡∏°‡πÉ‡∏ô ({channel})")
                    return

            # ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ó‡∏µ‡πà‡∏°‡∏µ‡∏õ‡∏∏‡πà‡∏°‡πÉ‡∏ô‡∏ä‡πà‡∏≠‡∏á‡∏ô‡∏±‡πâ‡∏ô ‡πÉ‡∏´‡πâ‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÉ‡∏´‡∏°‡πà
            await channel.send(embed=embed, view=VerifyButton(roles))
            print(f"‚úÖ ‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏¢‡∏∑‡∏ô‡∏¢‡∏±‡∏ô‡∏ï‡∏±‡∏ß‡∏ï‡∏ô‡πÑ‡∏õ‡∏¢‡∏±‡∏á {channel.name} ‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢!")

def setup(bot):
    bot.add_cog(Verify(bot))
